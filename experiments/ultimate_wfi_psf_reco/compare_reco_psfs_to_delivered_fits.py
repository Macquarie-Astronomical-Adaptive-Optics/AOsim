#!/usr/bin/env python3
"""
Compare reconstructed covariance-model PSFs against a delivered AOsim FITS PSF.

This is the next validation after validate_psf_from_covariance_models.py.

Two-stage use
-------------

1. List candidate delivered PSF FITS files:

    python experiments/ultimate_wfi_psf_reco/compare_reco_psfs_to_delivered_fits.py \
        "$RUN_DIR" \
        --list-fits

2. Pick the corrected delivered long-exposure science PSF FITS, then compare:

    python experiments/ultimate_wfi_psf_reco/compare_reco_psfs_to_delivered_fits.py \
        "$RUN_DIR" \
        --reference-fits "$RUN_DIR/path/to/corrected_psf.fits" \
        --models-dir "$RUN_DIR/psf_from_covariance_models" \
        --metric-crop 128 \
        --plot-crop 128 \
        --align-peak \
        --outdir "$RUN_DIR/psf_vs_delivered_fits" \
        --verbose

Inputs expected
---------------
- models-dir/psf_from_covariance_models.npz
  generated by validate_psf_from_covariance_models.py

- A delivered corrected PSF FITS file
  generated by the headless AOsim run

Caveat
------
This script compares images in pixel space. If the delivered FITS PSF and the
covariance-model PSFs have different focal-plane pixel scales, the comparison
will be qualitatively useful but not yet a final physical PSF-R metric.
"""

from __future__ import annotations

import argparse
import csv
import json
import re
from pathlib import Path
from typing import Any

import numpy as np
import matplotlib.pyplot as plt


def require_astropy():
    try:
        from astropy.io import fits  # type: ignore
        return fits
    except Exception as exc:
        raise RuntimeError(
            "This script needs astropy to read FITS files. "
            "Install it in the venv with: python -m pip install astropy"
        ) from exc


def load_json(path: Path) -> dict[str, Any]:
    if path.exists():
        with path.open("r", encoding="utf-8") as f:
            return json.load(f)
    return {}


def save_json(payload: dict[str, Any], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, allow_nan=True)


def finite_image(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=np.float64)
    while x.ndim > 2:
        x = x[0]
    x = np.nan_to_num(x, nan=0.0, posinf=0.0, neginf=0.0)
    return x


def normalize_psf(x: np.ndarray) -> np.ndarray:
    x = finite_image(x)
    x = np.maximum(x, 0.0)
    s = float(np.sum(x))
    return x / s if s > 0 else x


def center_crop(img: np.ndarray, size: int) -> np.ndarray:
    img = finite_image(img)
    h, w = img.shape
    size = int(min(size, h, w))
    y0 = (h - size) // 2
    x0 = (w - size) // 2
    return img[y0:y0 + size, x0:x0 + size]


def roll_peak_to_center(img: np.ndarray) -> np.ndarray:
    img = finite_image(img)
    if img.size == 0:
        return img
    iy, ix = np.unravel_index(np.argmax(img), img.shape)
    cy = img.shape[0] // 2
    cx = img.shape[1] // 2
    return np.roll(np.roll(img, cy - iy, axis=0), cx - ix, axis=1)


def prepare_for_metric(img: np.ndarray, crop: int, align_peak: bool) -> np.ndarray:
    img = normalize_psf(img)
    if align_peak:
        img = roll_peak_to_center(img)
    img = center_crop(img, crop)
    return normalize_psf(img)


def rel_l2(ref: np.ndarray, img: np.ndarray) -> float:
    ref = normalize_psf(ref)
    img = normalize_psf(img)
    den = float(np.linalg.norm(ref))
    return float(np.linalg.norm(img - ref) / den) if den > 0 else float("nan")


def l1(ref: np.ndarray, img: np.ndarray) -> float:
    return float(np.sum(np.abs(normalize_psf(img) - normalize_psf(ref))))


def peak_rel_error(ref: np.ndarray, img: np.ndarray) -> float:
    ref = normalize_psf(ref)
    img = normalize_psf(img)
    p = float(np.max(ref))
    return float((np.max(img) - p) / p) if p > 0 else float("nan")


def encircled_energy(img: np.ndarray, radius: float) -> float:
    img = normalize_psf(img)
    h, w = img.shape
    y, x = np.indices((h, w))
    cy = (h - 1) / 2.0
    cx = (w - 1) / 2.0
    r = np.sqrt((y - cy) ** 2 + (x - cx) ** 2)
    return float(np.sum(img[r <= radius]))


def radial_profile(img: np.ndarray, nbins: int | None = None) -> tuple[np.ndarray, np.ndarray]:
    img = normalize_psf(img)
    h, w = img.shape
    y, x = np.indices((h, w))
    cy = (h - 1) / 2.0
    cx = (w - 1) / 2.0
    r = np.sqrt((y - cy) ** 2 + (x - cx) ** 2)
    if nbins is None:
        nbins = int(np.floor(np.max(r))) + 1
    edges = np.linspace(0.0, float(np.max(r)), nbins + 1)
    rr = 0.5 * (edges[:-1] + edges[1:])
    prof = np.full(nbins, np.nan, dtype=float)
    for i in range(nbins):
        m = (r >= edges[i]) & (r < edges[i + 1])
        if np.any(m):
            prof[i] = float(np.mean(img[m]))
    return rr, prof


def safe_name(s: str) -> str:
    s = s.strip()
    s = re.sub(r"[^A-Za-z0-9_.+-]+", "_", s)
    return s[:160] if len(s) > 160 else s


def display_name_from_key(k: str) -> str:
    s = k
    s = s.replace("_plus_", " + ")
    s = s.replace("_plus", " +")
    s = s.replace("plus_", "+ ")
    s = s.replace("_", " ")
    s = s.replace("tel/POL", "tel/POL")
    s = re.sub(r"\s+", " ", s).strip()
    return s


def fits_candidate_score(path: Path) -> int:
    name = path.name.lower()
    score = 0
    for token in ("psf", "corrected", "corr", "delivered", "science", "sci", "long", "le", "coadd"):
        if token in name:
            score += 2
    for token in ("uncorrected", "uncorr", "noao", "raw", "truth", "phase", "pupil", "telemetry"):
        if token in name:
            score -= 3
    return score


def list_fits(run_dir: Path) -> list[dict[str, Any]]:
    fits = require_astropy()
    patterns = ("*.fits", "*.fit", "*.fts", "*.fits.gz")
    paths: list[Path] = []
    for pat in patterns:
        paths.extend(run_dir.rglob(pat))
    paths = sorted(set(paths))

    rows: list[dict[str, Any]] = []
    for p in paths:
        row: dict[str, Any] = {
            "path": str(p),
            "relative_path": str(p.relative_to(run_dir)) if p.is_relative_to(run_dir) else str(p),
            "score": fits_candidate_score(p),
        }
        try:
            data = finite_image(fits.getdata(p))
            row.update({
                "shape": list(data.shape),
                "sum": float(np.sum(data)),
                "peak": float(np.max(data)) if data.size else float("nan"),
                "min": float(np.min(data)) if data.size else float("nan"),
                "max_location": list(map(int, np.unravel_index(np.argmax(data), data.shape))) if data.size else None,
            })
        except Exception as exc:
            row["error"] = str(exc)
        rows.append(row)

    rows.sort(key=lambda r: (r.get("score", 0), str(r.get("relative_path", ""))), reverse=True)
    return rows


def print_fits_list(rows: list[dict[str, Any]]) -> None:
    if not rows:
        print("No FITS files found.")
        return

    print("\nCandidate FITS files, sorted by rough filename score:\n")
    for i, r in enumerate(rows):
        shape = r.get("shape", "?")
        peak = r.get("peak", float("nan"))
        total = r.get("sum", float("nan"))
        loc = r.get("max_location", None)
        err = r.get("error")
        print(f"[{i:02d}] score={r.get('score', 0):+d}  {r.get('relative_path')}")
        if err:
            print(f"     ERROR: {err}")
        else:
            print(f"     shape={shape}  sum={total:.6g}  peak={peak:.6g}  maxloc={loc}")
    print("\nPick the corrected delivered long-exposure science PSF, then rerun with:")
    print('  --reference-fits "$RUN_DIR/<relative_path>"')


def load_models(models_dir: Path, models_npz: Path | None) -> tuple[dict[str, np.ndarray], Path]:
    if models_npz is None:
        models_npz = models_dir / "psf_from_covariance_models.npz"
    if not models_npz.exists():
        raise FileNotFoundError(f"Model PSF NPZ not found: {models_npz}")

    z = np.load(models_npz)
    models: dict[str, np.ndarray] = {}
    for k in z.files:
        models[display_name_from_key(k)] = normalize_psf(z[k])
    return models, models_npz


def compute_metrics(ref: np.ndarray, img: np.ndarray, ee_radii: list[float]) -> dict[str, float]:
    out = {
        "rel_l2": rel_l2(ref, img),
        "l1": l1(ref, img),
        "peak_rel_error": peak_rel_error(ref, img),
        "peak_ref": float(np.max(normalize_psf(ref))),
        "peak_model": float(np.max(normalize_psf(img))),
    }
    for r in ee_radii:
        ee_ref = encircled_energy(ref, r)
        ee_img = encircled_energy(img, r)
        out[f"ee_ref_r{r:g}"] = ee_ref
        out[f"ee_model_r{r:g}"] = ee_img
        out[f"ee_abs_error_r{r:g}"] = ee_img - ee_ref
        out[f"ee_rel_error_r{r:g}"] = (ee_img - ee_ref) / ee_ref if ee_ref > 0 else float("nan")
    return out


def plot_radial(ref: np.ndarray, prepared: dict[str, np.ndarray], outdir: Path) -> None:
    fig, ax = plt.subplots(figsize=(8, 5), dpi=150)
    rr, prof = radial_profile(ref)
    ax.semilogy(rr, np.maximum(prof, 1e-30), label="delivered FITS", linewidth=2.5)
    for name, img in prepared.items():
        rr, prof = radial_profile(img)
        ax.semilogy(rr, np.maximum(prof, 1e-30), label=name)
    ax.set_xlabel("Radius [pixels in comparison crop]")
    ax.set_ylabel("Azimuthal mean normalized PSF")
    ax.set_title("Radial profiles versus delivered FITS PSF")
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=7)
    fig.tight_layout()
    fig.savefig(outdir / "radial_profiles_vs_delivered_fits.png")
    plt.close(fig)


def plot_grid(ref: np.ndarray, prepared: dict[str, np.ndarray], outdir: Path) -> None:
    all_images = {"delivered FITS": ref, **prepared}
    names = list(all_images.keys())
    ncols = 3
    nrows = int(np.ceil(len(names) / ncols))
    fig, axes = plt.subplots(nrows, ncols, figsize=(4.2 * ncols, 3.9 * nrows), dpi=150)
    axes = np.asarray(axes).ravel()
    vmax = float(np.max(ref))
    vmin = max(vmax * 1e-5, 1e-30)

    for ax, name in zip(axes, names):
        img = normalize_psf(all_images[name])
        ax.imshow(np.log10(np.maximum(img, vmin)), origin="lower", vmin=np.log10(vmin), vmax=np.log10(vmax))
        ax.set_title(name, fontsize=9)
        ax.set_xticks([])
        ax.set_yticks([])

    for ax in axes[len(names):]:
        ax.axis("off")

    fig.suptitle("Model PSFs and delivered FITS PSF, log10 scale")
    fig.tight_layout()
    fig.savefig(outdir / "psf_grid_vs_delivered_fits.png")
    plt.close(fig)


def plot_residuals(ref: np.ndarray, prepared: dict[str, np.ndarray], outdir: Path) -> None:
    names = list(prepared.keys())
    ncols = 3
    nrows = int(np.ceil(len(names) / ncols))
    fig, axes = plt.subplots(nrows, ncols, figsize=(4.2 * ncols, 3.9 * nrows), dpi=150)
    axes = np.asarray(axes).ravel()
    scale = max(float(np.max(ref)), 1e-30)

    for ax, name in zip(axes, names):
        diff = normalize_psf(prepared[name]) - normalize_psf(ref)
        ax.imshow(diff / scale, origin="lower", vmin=-0.1, vmax=0.1)
        ax.set_title(f"{name} - delivered", fontsize=9)
        ax.set_xticks([])
        ax.set_yticks([])

    for ax in axes[len(names):]:
        ax.axis("off")

    fig.suptitle("Residuals relative to delivered FITS, normalized by delivered peak")
    fig.tight_layout()
    fig.savefig(outdir / "psf_residuals_vs_delivered_fits.png")
    plt.close(fig)


def write_csv(metrics: dict[str, dict[str, float]], path: Path) -> None:
    keys = sorted({k for m in metrics.values() for k in m.keys()})
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(["model", *keys])
        for model, m in metrics.items():
            w.writerow([model, *[m.get(k, "") for k in keys]])


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Compare reconstructed covariance-model PSFs to a delivered FITS PSF.")
    p.add_argument("run_dir", type=Path)
    p.add_argument("--list-fits", action="store_true")
    p.add_argument("--reference-fits", type=Path, default=None)
    p.add_argument("--models-dir", type=Path, default=None)
    p.add_argument("--models-npz", type=Path, default=None)
    p.add_argument("--outdir", type=Path, default=None)

    p.add_argument("--metric-crop", type=int, default=128)
    p.add_argument("--plot-crop", type=int, default=128)
    p.add_argument("--align-peak", action="store_true")
    p.add_argument("--ee-radii", type=float, nargs="*", default=[4, 8, 16, 32, 64])
    p.add_argument("--verbose", action="store_true")
    return p.parse_args()


def main() -> None:
    args = parse_args()
    run_dir = args.run_dir.expanduser().resolve()

    if args.list_fits:
        rows = list_fits(run_dir)
        print_fits_list(rows)
        out = run_dir / "fits_candidates_summary.json"
        save_json({"run_dir": str(run_dir), "fits_candidates": rows}, out)
        print(f"\nWrote: {out}")
        if args.reference_fits is None:
            return

    if args.reference_fits is None:
        raise SystemExit("Provide --reference-fits, or run first with --list-fits.")

    fits = require_astropy()
    ref_path = args.reference_fits.expanduser().resolve()
    models_dir = args.models_dir.expanduser().resolve() if args.models_dir else run_dir / "psf_from_covariance_models"
    outdir = args.outdir.expanduser().resolve() if args.outdir else run_dir / "psf_vs_delivered_fits"
    outdir.mkdir(parents=True, exist_ok=True)

    models, models_npz = load_models(models_dir, args.models_npz)
    ref_raw = normalize_psf(fits.getdata(ref_path))

    metric_crop = int(min(args.metric_crop, ref_raw.shape[0], ref_raw.shape[1], *(img.shape[0] for img in models.values()), *(img.shape[1] for img in models.values())))
    plot_crop = int(min(args.plot_crop, ref_raw.shape[0], ref_raw.shape[1], *(img.shape[0] for img in models.values()), *(img.shape[1] for img in models.values())))

    ref_metric = prepare_for_metric(ref_raw, metric_crop, args.align_peak)
    prepared_metric: dict[str, np.ndarray] = {}
    for name, img in models.items():
        prepared_metric[name] = prepare_for_metric(img, metric_crop, args.align_peak)

    metrics: dict[str, dict[str, float]] = {}
    for name, img in prepared_metric.items():
        metrics[name] = compute_metrics(ref_metric, img, args.ee_radii)

    # Separate plot crops so plots can be a different size from metric crop.
    ref_plot = prepare_for_metric(ref_raw, plot_crop, args.align_peak)
    prepared_plot = {name: prepare_for_metric(img, plot_crop, args.align_peak) for name, img in models.items()}

    plot_radial(ref_plot, prepared_plot, outdir)
    plot_grid(ref_plot, prepared_plot, outdir)
    plot_residuals(ref_plot, prepared_plot, outdir)

    write_csv(metrics, outdir / "metrics_vs_delivered_fits.csv")

    summary = {
        "run_dir": str(run_dir),
        "reference_fits": str(ref_path),
        "models_npz": str(models_npz),
        "outdir": str(outdir),
        "reference_shape_raw": list(ref_raw.shape),
        "model_shapes_raw": {name: list(img.shape) for name, img in models.items()},
        "metric_crop": metric_crop,
        "plot_crop": plot_crop,
        "align_peak": bool(args.align_peak),
        "ee_radii_px": list(args.ee_radii),
        "metrics_vs_delivered_fits": metrics,
        "caveat": (
            "This is a pixel-space comparison. Confirm that the delivered FITS PSF "
            "and covariance-model PSFs have the same focal-plane sampling before "
            "using these as final physical metrics."
        ),
    }
    save_json(summary, outdir / "summary_vs_delivered_fits.json")

    print("\n=== Model PSFs vs delivered FITS PSF ===")
    print(f"Run dir:        {run_dir}")
    print(f"Reference FITS: {ref_path}")
    print(f"Models NPZ:     {models_npz}")
    print(f"Output:         {outdir}")
    print(f"Raw reference shape: {ref_raw.shape}")
    print(f"Metric crop: {metric_crop} px")
    print(f"Peak alignment: {args.align_peak}")

    print("\nMetrics vs delivered FITS:")
    ranked = sorted(metrics.items(), key=lambda kv: kv[1]["rel_l2"])
    for name, m in ranked:
        print(
            f"  {name:36s} "
            f"L2={100*m['rel_l2']:7.2f}%  "
            f"L1={m['l1']:.4f}  "
            f"peak_err={100*m['peak_rel_error']:+7.2f}%"
        )

    print("\nWrote:")
    for p in (
        outdir / "summary_vs_delivered_fits.json",
        outdir / "metrics_vs_delivered_fits.csv",
        outdir / "radial_profiles_vs_delivered_fits.png",
        outdir / "psf_grid_vs_delivered_fits.png",
        outdir / "psf_residuals_vs_delivered_fits.png",
    ):
        print(f"  {p}")

    if args.verbose:
        print("\nReminder: if all metrics look bad, first check whether the delivered FITS PSF")
        print("has the same sampling/normalization/cropping convention as the model PSFs.")


if __name__ == "__main__":
    main()
